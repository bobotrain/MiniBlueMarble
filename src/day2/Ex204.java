package day2;

public class Ex204 {
public static void main(String[] args) {
	
	// 산술연산자 : + - * / %
	// 대입연산자 : = (오.정.왼.담.) 
	// 대입연산자는 반드시 양변 타입 일치시켜야 함. 불일치 하는 경우에는 자동형 변환 고려해서 담을 수 있는 그릇인지 여부파악.
	int num;
	num = 2-3;
	
	// 산술  + 대입 연산자 : += -= *= /= %= 
	
	num += 3;
	num -= 1;
	num *= 7;
	num /= 3;
	num %= 5;
	
	System.out.println(num);
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
}
